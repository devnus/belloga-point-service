package com.devnus.belloga.point.common.exception.error;

public class InsufficientDrawConditionException extends RuntimeException {
    public InsufficientDrawConditionException() {
        super();
    }
    public InsufficientDrawConditionException(String message, Throwable cause) {
        super(message, cause);
    }
    public InsufficientDrawConditionException(String message) {
        super(message);
    }
    public InsufficientDrawConditionException(Throwable cause) {
        super(cause);
    }
}
